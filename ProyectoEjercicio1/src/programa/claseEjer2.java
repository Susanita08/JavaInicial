public class claseEjer2 {
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public double getN() {
		return N;
	}
	public void setN(double n) {
		N = n;
	}
	public double getM() {
		return M;
	}
	public void setM(double m) {
		M = m;
	}
	private int x=0;
	private int y=2;
	private double N=50;
	private double M=20.5;
	
	
}
